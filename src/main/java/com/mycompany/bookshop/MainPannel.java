    private void bookaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookaddActionPerformed

   String bookName = BookName.getText();
    String authorName = authorNameField.getText();
    int totalStock = Integer.parseInt(totalStockField.getText());
    int price = Integer.parseInt(priceField.getText());
    if (bookName.isEmpty() || authorName.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Book Name and Author Name cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    BookManagement.getInstance().addBook(bookName, authorName, totalStock, price);
    BookName.setText("");
    authorNameField.setText("");
    totalStockField.setText("");
    priceField.setText("");
    all_table_model_loader();
    }//GEN-LAST:event_bookaddActionPerformed

