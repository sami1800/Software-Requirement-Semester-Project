     private void sellBook() {
        try {
            int regNo = Integer.parseInt(regidsale.getText());
            int copy=Integer.parseInt(noOfBook.getText());
            SalesReportGenerator.sellBook(regNo,copy, data);
            JOptionPane.showMessageDialog(this, "Book sold successfully.");
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter a valid registration number.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "An error occurred while selling the book.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
